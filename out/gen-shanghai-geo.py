def build_geojson():
    features = []
    for name, adcode, clon, clat, ring in DISTRICTS:
        # Close the ring if not already closed
        if ring[0] != ring[-1]:
            ring = ring + [ring[0]]

        # Reverse to ensure CCW winding (GeoJSON spec)
        ring = list(reversed(ring))

        # Wrap in proper GeoJSON MultiPolygon structure
        coords = [[ring]]  # MultiPolygon → 1 polygon → 1 ring

        feat = {
            "type": "Feature",
            "properties": {
                "adcode": adcode,
                "name": name,
                "center": [clon, clat]
            },
            "geometry": {
                "type": "MultiPolygon",
                "coordinates": coords
            }
        }
        features.append(feat)

    return {"type": "FeatureCollection", "features": features}

# Generate and save
result = build_geojson()
with open('assets/data/shanghai.json', 'w', encoding='utf-8') as f:
    json.dump(result, f, ensure_ascii=False)

# Stats
total_pts = sum(len(f['geometry']['coordinates'][0][0]) for f in result['features'])
print(f"Generated {len(result['features'])} districts with {total_pts} total points")

# Verify winding
def signed_area(ring):
    area = 0
    n = len(ring)
    for i in range(n):
        x1, y1 = ring[i]
        x2, y2 = ring[(i+1)%n]
        area += x1*y2 - x2*y1
    return area / 2

cw = ccw = 0
for f in result['features']:
    ring = f['geometry']['coordinates'][0][0]
    a = signed_area(ring)
    if a > 0:
        ccw += 1
    else:
        cw += 1
print(f"Winding: {ccw} CCW, {cw} CW {'OK' if cw==0 else 'BAD'}")

